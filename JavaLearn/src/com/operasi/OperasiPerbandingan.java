package com.operasi;

public class OperasiPerbandingan {
	public static void main(String[] args) {
		/* 
		  // operator perbandingan
		    operasi perbandinga adlah operasi untuk membandingkan dua buah data
		    operasi perberbandingan mmenghasilkan boolean (true/false)
		    Jika nilainya benar maka hasilnya true 
		    jiak nilainya salah hasilnya false 
		    
		    operator 
		    > lebih dari
		    < kurang dari
		    >= lebih dari sama dengan 
		    <= kurang dari sama dengan
		    == sama dengan
		    !== tidak sama dengan 
		 */
		
		int num = 100 ;
		int numm = 100 ;
		
		System.out.println(num > numm);
		
		System.out.println(num < numm);
		
		System.out.println(num >= numm);
		
		System.out.println(num <= numm);
		
		System.out.println(num == numm);
		
		System.out.println(num != numm);
		}
}
