package com.latihan ;

public class OperasiLatihan {
	public static void main(String[] args) {
		// operasu perbandingan
		float myNumber = 300f ;
		int myInteger = 200;
		
		System.out.println(myNumber > myInteger);
		
		System.out.println(myInteger < myNumber);
		
		System.out.println(myNumber <= myInteger);
		
		System.out.println(myNumber >= myInteger);
		
		System.out.println(myNumber == myInteger);
		
		System.out.println(myNumber != myInteger);
		
		String[] Person ;
		Person = new String[] {
		"Sugilar",  
		};
		
		System.out.println(Person.length);
	}
}
