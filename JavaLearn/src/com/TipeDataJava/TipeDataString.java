package com.TipeDataJava ;

public class TipeDataString {  
	public static void main(String[] args) {
		// tipe data string
		// tipe data string adalah tipe data yang berisikan kumpulan banyak character atau sederhananya adalah text
		// Di Java, tipe data string direpsenstasikan dengan String
		// untuk membuat string kita bisa menggunakan petik petik dua ""
		// default dari string adalah null
		
		String txt = "nama saya sugilar";
		
		System.out.println(txt);
		
		String firstName = "Ardi";
		String lastName = "Sugilar";
		String fullName = firstName + " " + lastName;
		System.out.println(fullName);
	}
}