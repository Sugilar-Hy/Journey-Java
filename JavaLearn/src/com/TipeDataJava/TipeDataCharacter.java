package com.TipeDataJava;

public class TipeDataCharacter {
	public static void main(String[] args) {
		
		// tipe data character 
		// Data character (huruf) di presentasikan oleh tipe char 
		// untuk membuat tipe data char di java, kita bsia menggunakan tanda petik satu '' 
	
		char s  = 'S' ;
		char u  = 'U' ;
		char g  = 'G' ;
		char i  = 'I' ;
		char l	= 'L' ;
		char a	= 'A' ;
		char r	= 'R' ;
		
		System.out.print(s);
		System.out.print(u);
		System.out.print(g);
		System.out.print(i);
		System.out.print(l);
		System.out.print(a);
		System.out.print(r);
		
	}
}
