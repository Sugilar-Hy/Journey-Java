package com.TipeDataJava ;


public class TipeDataboolean {
	public static void main(String[] args) {
		// tipe data boolean 
		// Tipe data boolean adalah tipe data yangi memiliki 2 nilai, yaitu benar atau salah
		// tipe data boolean direpresentasikan dengan kata kunci boolean
		// nilai benar direpsentasikan dengan true 
		// nilai salah direpsenatasikan dengan false
		// nilai default fari tipe data boolean adalah false
		
		
		boolean benar = true ;
		boolean salah = false ;
		
		System.out.println(benar);
		System.out.println(salah);
		System.out.print("tipe data boolean");
	}
}